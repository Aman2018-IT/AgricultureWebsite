            <p style="color: white;font-size: 30px; text-align: left;"><i>A mango is an edible stone fruit produced by the tropical tree Mangifera indica which is believed to have originated from the region between northwestern Myanmar, Bangladesh, and northeastern India. </i></p>
        
        
    <div class="Weather">
        <h2 style=" font-size: 30px; text-align: left; color: white;"><strong><i>Weather Conditions:</i></strong></h2>
            <p style="color: white;font-size: 20px;">Mango is very well adapted to tropical and subtropical climates. It thrives well in almost all the regions
                0 0 of the country from sea level to an altitude of 600 m. The ideal temperature range for mango is 24 -30 C
                during the growing season, along with high humidity. A rainfall range of 890-1,015 mm in a year is
                considered as ideal for growing mangoes. However, mango can be grown in regions of both heavy
                (2540 mm) or scanty (254 mm) rainfall. Dry weather before blossoming is conducive to profuse
                flowering. Rain during flowering is detrimental to the crop as it interferes with pollination. </p>      
    </div>

    <div class="Soil">    
        <h3 style=" font-size: 30px; text-align: left; color: white;"><strong><i>Soil requirement:</i></strong></h3>        
            <p style="color: white;font-size: 20px;">Fog, cloudy weather at the time of flowering from November to February results in poor setting of fruits
                and favours pest and disease incidence. Mango grows well on wide variety of soils, such as lateritic,
                alluvial, sandy loam and sandy.The loamy, alluvial, well-drained, aerated and deep soils (2-2.5 m) rich
                in organic matter with a pH range of 5.5-7.5 are ideal for mango cultivation. The water table should be
                around 3 m and soils with high water table are unsuitable for mango. </p>
    </div>        

