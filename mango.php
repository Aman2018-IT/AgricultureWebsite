    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                <p style="color: white;font-size: 20px;">
                        
                        <strong><u>Planting</u> :</strong><br> should be prepared by deep ploughing followed by harrowing and levelling with
                        a gentle slope for good drainage. Spacing varies from 10 m x 10 m, in the dry zones where growth is less, to 12 m x 12 m, in heavy rainfall areas and rich soils where abundant vegetative growth occurs. New dwarf hybrids like Amrapali can be planted at closer spacing. Pits are filled with original soil mixed with 20-25 kg well rotten FYM, 2.5 kg single super phosphate and 1 kg muriate of potash.<br>

                        One year old healthy, straight growing grafts from reliable sources can be planted at the centre of pits along with the ball of the earth intact during rainy season in such a way that the roots are not expanded and the graft union is above the ground level. Plants should be irrigated immediately after planting. In the initial one or two years, it is advisable to provide some shade to the young plants and also stake to make them grow straight.<br><br>

                        <strong><u>Training and pruning</u>:</strong><br>
                        About one meter from the base on the main trunk should be kept free from branching and the main stem can be allowed thereafter spaced at 20-25 cm apart in such a way that they grow in different directions. Branches which cross over/rub each other may be removed at pencil thickness.<br><br>

                       <strong><u>Fertiliser Application</u>:</strong><br>
                        In general, 170 gm urea, 110 gm single super phosphate and 115 gm muriate of potash per plant per year of the age from first to tenth year and thereafter 1.7 kg, 1.1 kg, and 1.15 kg respectively of these fertilisers per plant per year can be applied in two equal split doses (June-July and October). Foliar spray of 3% urea is recommended before flowering in sandy areas.<br><br>

                       <strong><u>Irrigation</u>:</strong><br>
                        Young plants are watered frequently for proper estalbishment. In case of grown up trees, irrigation at 10 to 15 days interval from fruit set to maturity is beneficial for improving yield.However, irrigation is not recommended for 2-3 months prior to flowering as it is likely to promote vegetative growth at the expense of flowering.<br><br>

                       <strong><u>Inter cropping</u>:</strong><br>
                       Inter crops such as vegetables, legumes, short duration and dwarf fruit crops like papaya, guava,peach, plum, etc. depending on the agro-climatic factors of the region can be grown. The water and nutrient requirements of the inter crops must be met separately.<br><br>

                       <strong><u>Plant Protection</u>:</strong><br>
                       Mango is prone to damages by a large number of pests, diseases and disorders.<br> 
                       The recommended control measures for most important and common among them are briefed below :<br><br>
                       
                       <strong>Mango hopper :</strong> Two sprays (at panicles emergency and at pea size of fruits) of carbaryl (0.15%), monocrotophos (0.04%) or phosphamidan (0.05).<br>
                       
                       <strong>Mealy bug :</strong> Ploughing inter spaces in November and dusting 2% methyl parathion @200 g per tree near the trunk and fixing 20 cm wide 400 gauge polythene strips around the trunk with grease applied on the lower edge in January as prophylactic measures and two sprays of monocrotophos (0.04%) at 15 days interval as control are needed.<br>
                       
                       <strong>Powdery mildew :</strong> Two to three sprays of wettable sulphur (0.2%) or Kerathane (0.1%) at 10-15 days interval.<br>
                       
                       <strong>Anthracrose :</strong> Two sprays of Baristin (0.1%) at fortnight interval.<br>

                       <strong>Malformation :</strong> One spray of 200 ppm NAA in October followed by deblossoming at bud burst stage in December - January.<br>

                       <strong>Fruit drop :</strong> Regular irrigation during fruit development, timely and effective control of pests and diseases and spraying 20 ppm NAA at pea size of fruits.<br><br>

                       <strong><u>Harvesting and yield </u>:</strong><br>
                       Graft plants start bearing at the age of 3 - 4 years (10-20 fruits) to give optimum crop from 10- 15th year which continues to increase upto the age of 40 years under good management.<br><br>

                       <strong><u>Post Harvest Management </u>:</strong><br>
                       Storage : Shelf life of mangoes being short (2 to 3 weeks) they are cooled as soon as possible to storage temperatue of 13 degree Celcius. A few varieties can withstand storage temperature of 10 degree Celcius. Steps involved in post harvest handling include preparation, grading, washing,drying, waxing, packing, pre-cooling, palletisation and transportation.
        </p>
