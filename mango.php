            5.<u><strong>Alphonso/Hapus</u>:</strong>
            Alphonso mango is unlike any other type of mango and is the king of all mangoes. It is a native to Maharashtra but is also grown in some parts of Gujarat, Karnataka and Madhya Pradesh. It is the most expensive variety of mango due to its distinct taste, color, and shape. 50-60 kg/tree.<br><br>
            
           6.<u><strong>Dasheri</u> :</strong>
