        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation </u>:</i></strong><br>
                            Capsicum is a cool season crop, but it can be grown round the year using protected structures where temperature and relative humidity (RH) can be manipulated. This crop requires day temperature of 25-300C and night temperature of 18-200C with relative humidity of 50-60%. If temperature exceeds 350C or falls below 120C, fruit setting is affected. <br> <br>

                            The land should be thoroughly ploughed and soil should be brought to fine tilth. Well decomposed organic manure at the rate of 20-25 kg per sq mtr is mixed with soil. One application is sufficient to grow three capsicum crops successively. Raised beds are formed after bringing soil to fine tilth. The bed size should be 90-100 cms wide and 15-22 cms height. Between the beds walking space of 45 cms to 50 cm need to be provided.


                          
                                 <br><br>
                        <p style="color: white;font-size: 17px;"><strong><i>2.<u>Irrigation</u>:</i></strong><br>
                            To reduce the pH of water, add the acid to the water tank,  then use water for irrigation and sprinkling.<br> <br> 

                            1.Immediately after plantation,  irrigation required to start the first shower is required to use after some days drip irrigation use this will help for uniform root development of a plant,<br> <br>
                            2.Usually, a dripper per plant is required. Depending on the season, drip irrigation provides 2-4 liters of water per square meter. In hot summer, a fogger can be used to maintain the air humidity.<br> <br>
                            3.Inspect the soil column for irrigation and visually check the soil moisture content. After this, determine the amount of irrigation required.<br> <br>
                            4. During the summer season, apply water to the edges of the bed repeatedly by using the shower to reduce the evaporator’s loss. For this purpose, provision for a water outlet (1 “diameter pipe) inside the  Greenhouse should be made.<br> <br>
                           5. Always pour the plant water from noon.<br> <br>
                            6.Relative humidity of air should not be more than 90 – 92 percent because it is inclined to disturb the fruit.<br> <br>
                           7. Always use fresh water for irrigation. Do not store water for 4 to 5 days.
                          
                                 <br><br>
                        <p style="color: white;font-size: 17px;">
                            <strong><i>3.<u> Fertilizers application</u>:</i></strong><br>
                            A basal fertilizer dose of 20:25:20 NPK is required per acre and is applied to the beds uniformly before transplanting in the form of 80 kg calcium ammonium nitrate, 125 kg super phosphate and 32 kg murate of potash or 40 kg sulphate of potash. <br><br>
                            
                           
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>4.<u>Harvesting</u>:</i></strong><br>
                            
                                Early morning hours are best suited for capsicum harvest. Green capsicum can be harvested at 55 to 60 days after transplanting, yellow capsicum at 70-75 days whereas red capsicum at 80-90 days. Fruits can be harvested once in 3 to 4 days. Yellow and red fruits can be harvested when they have gained 22 50-80 per cent of the colour development. After harvest fruits should be kept in cool place and avoid direct exposure to sunlight. The fruits should be handled carefully by adopting clip harvest technique and scuffing should be minimized. The average yield of capsicum per acre is 30-40 tons.
