 <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
        <p style="color: white;font-size: 20px;">
        
        <strong><i><u>Land preparation</u></i></strong>:<br><br>
        After kharif crop harvesting, to obtain fine seed bed do one to two ploughing. Along with ploughing take 2-3 harrowing and do planking after ploughing operation. Field should be level to avoid waterlogging conditions in soil. Give pre-sowing irrigation before sowing of crop, it will help in good germination of crop.<br><br>

        <strong><i><u>Sowing</u></i></strong>:<br><br>
        <strong><i>Time of sowing</strong></i><br>
         To obtain good yield complete sowing between October end to Middle of November. Further delay in sowing will lead to yield loss. For early market, some growers sow peas in second fortnight of October.<br><br>

        <strong><i>Spacing:</i></strong>
         Use spacing of 30cmx5cm for early varieties and for late varieties use spacing of 45-60cm x 10cm.<br>
        <strong><i>Sowing Depth:</i></strong>Sow the seeds at depth of 2-3cm in soil.<br>
        <strong><i>Method of sowing:</i></strong>For sowing use seed cum fertilizer drill on ridges which are 60 cm wide.<br>

        <strong><i><u>Chemical weed control</u></i></strong>:<br><br>
        Depending upon variety it requires one or two weeding. First weeding is done either at 2-3 leaves stage or 3-4 weeks after sowing and second weeding is done before flowering. Using herbicides is the effective method of controlling weeds in peas cultivation. Pendimethalin@1 ltr/acre and Basalin@1 ltr/acre give good results in controlling the weeds. Apply herbicide within 48 hours of sowing.<br><br>


       <strong><i><u>Irrigation</u></i></strong>:<br><br>
       Pre sowing irrigation should be given for good germination. When it is cultivated after paddy crop and soil content sufficient moisture, it can sow without irrigation. After sowing it required one or two irrigation. Apply first irrigation during pre-flowering and second at pod formation stage. Heavy irrigation leads to the yellowing of plants and thus reducing the yield.<br><br>

       <strong><i><u>Harvesting</u></i></strong>:<br><br>
       The green and white peas pods should be harvested at proper stage. Harvesting of peas may start as soon as peas started changing colour from dark to green. Multiple pickings like, 4 to 5 pickling can be done within the 6 to 10 days interval. Yield depends on the variety, soil fertility and management of the field.<br><br>
       

       <strong><i><u>Post-Harvest</u></i></strong>:<br><br>
       Storage is done at low temperature to increase the availability of green pods for longer duration. Packing is done in gunny bags, corrugated fibre board boxes, plastic containers and bamboo baskets.
        </p>
