    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 20px;"><u>Land preparation</u>:<br><br>
                            1. Good land preparations are necessary for better yield of black gram.<br>
                            2. Land should be ploughed in summer followed by two to three harrowing at pre-monsoon for kharif season.<br>
                            3. For summer black gram, after harvest of rabi crops, field prepare with criss-cross moghda once followed by two harrowing.<br>
                            4. Land should be well leveled.<br>
                            5. Collect the weeds, previous crops stubbles, stones and clean the field for sowing.<br>
                            6. Mix five to six tons of FYM or compost at last harvesting.<br>
                            7. If necessary, one pre-sowing irrigation should be given.<br><br>
                            
                            
                            <u>Sowing</u>:<br>a. Selection of seed:<br><br>
                            1. Seed should be free from insect, pest and diseases.<br>
                            2. It should be free from any inert matter i.e. dust particles, weed seeds etc.<br>
                            3. It should be purchased from Agril. Research station, universities, KVKâ€™s and registered seed companies /Agri. Service centers/Agriclinics.<br>
                            4. Seeds should be viable and genetically pure.<br>
                            <br>
                            b. Method of sowing:<br><br>
                            1. Sowing should be done in line sowing\drilling method with tractor drawn ferti-cum-seed drill or by bullock drawn ferti-cum-seed drill/tifan.<br>
                            2. Ferti-cum-seed drill should be used for the application of fertilizers and seed sowing at a time.<br>
                            3. Seeds should not be sown more than 5 to 6 cm in depth.<br>
                            <br>
                            c. Time of sowing:<br><br>
                            1. Second fortnight of June (15to 30 June) is the proper time of black gram sowing in kharif season.<br>
                            2. In summer, sowing should be done from third week of Feb. to First week of April.<br>
                            3. Late sowing should be avoided<br>
                            <br>
                            d. Seed treatment:<br><br>
                            1. Seed should be treated with thiram @ 2.5 g / kg seed
                            2. It should be treated with rhizobium culture for atmospheric N fixation.
                            <br><br>
                            <u>Irrigation management</u>:<br>
Irrigation is not needed in rainy season, but in summer season irrigation should be given as per critical stages and availability of irrigation water. Number and frequency of irrigation depend upon the soil type and weather. The crop should get irrigation at an interval of 10-15 days. From flowering to pod development stage, there is need of sufficient moisture in the field.
<br><br>

<u>Weed management</u>:
One or two hand weeding should be done up to 40 days of sowing depending upon the weed intensity. Weeds can be controlled by the use of herbicides i.e. Fluchloralin (Basalin) 1 kg a.i. / ha in 800-1000 litres of water as pre-planting application.
<br><br>
<u>Signs of maturity, harvesting & threshing</u>:<br>
Pods and plant dried, Grains become hard, and moisture percent in grain at harvesting should be 20-22 %. Pod shattering is common problem in pulse. Therefore picking should be done as soon as pods mature. Harvesting should be done 2-3 pickings. The pods or whole crop after complete drying should be threshed manually or by machine.
<br><br>

<u>Yield</u>:
A well managed crop, as indicated above, may produce 12 to 15 quintals of grain per hectare.

