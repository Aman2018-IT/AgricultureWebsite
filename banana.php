    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;">
                            <strong><i>1.<u>Planting</u>:</i></strong><br>
                            Planting of banana is done by two methods viz. Pit method and furrow method. Planting is done from February to May whereas in North India, it is done during July-August. In South-India, it can be done any time of year except summer. Tall varieties should be planted at 3x3 m whereas dwarf ones at 2x2 m apart.
                       <br><br>
                       <strong><i>2.<u>Manuaring</u>:</i></strong><br>
                       Banana is a heavy feeder and responds well to manuring. Banana is a quick growing and short-lived plant. Therefore, it is more beneficial, if quick growing fertilizers are applied. In Tamil Nadu, application of 100 g of N, 30 g of P2O5 and 300 g of K2O per plant to be supplied in three doses during the second, third and fifth months is recommended. In Maharashtra, 100 g N, 40 g P2O5 and 100 g K2O per plant is recommended. Out of these, P2O5 and K2O is applied at the time of planting and N is given is three split doses: third, fourth and fifth month after planting.
                  <br><br>
                  <strong><i>3.<u>After Cares</u>:</i></strong><br>
                  <i>1. Desuckering</i>:
                 Desuckering is an important operation in banana cultivation. In this, the unwanted suckers, which develop near the base of the plant, are removed. Cutting the suckers from ground level and then pouring the kerosene (2-3 drops) to kill the growing point of pseudostem does it.
                  <i>2. Propping</i>:
                  It is an essential cultural practice to give proper support to the plants with bamboos. It is done to avoid falling down of plants due to high winds.

                 <i>3. Wrapping</i>:
                 to protect the fruits from sunburn, hot wind and dust the bunch is covered. Wrapping is also done to improve the colour of the fruit.
                 <br><br>
                 <strong><i>4.<u>Plant-Protection</u>:</i></strong><br>
                 <i><strong> >>Disease</strong> </i>:
                 Important diseases of banana are:
                <br> <i>1. Panama wilt</i>:
                 It is caused by the soil bourne fungi Fusarium oxysporum.
                 <br>Symptoms: Leaves become yellow, Pseudostem splits and breaks.
                 <br>Control Measures: Eradication of the infected plants. Growing resistant varieties like Dwarf Cavendish. Soil drenching with Vapam @ 0.85% and mercuric chloride @ 3000 PPM in nursery.
                  <br><br>
                <i>2. Bunchy Top</i>:
                It is caused by the virus, which is transmitted by an aphid Pentalonia nigroneruosa. 
               <br><u> Symptoms</u>: Infected plants have short, narrow, erect leaves with short petiole. Plants remain slanted.
           <br>  <u>Control Measures</u>: Infected plants should be uprooted and burnt. Spraying of 2-4, D to control weeds.
               
             <br><br><i><strong> >>Insect-Pests</strong> </i>:
             <br><u>Root stock weevil</u>:- Damaging stage is adult and grub
             <br><u>Symptoms of damage</u>:- Grub bores into stem which is affected by fungi or bacteria.
           <br> <u>Control measures</u>:- Use healthy suckers and rhizomes. Before planting treat the pit with 0.65 %. Lindane 60-80 g/pit. Soak the sucker in 0.1% Lindane solution, spray 0.05 % endosulfan.
