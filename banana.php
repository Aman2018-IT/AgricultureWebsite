                    <strong><i><p style="color: white;font-size: 20px;">1.<u>Chlorpyrifos</u>:-</i></strong><br> 
                    Chlorpyrifos is an organophosphate insecticide that has been used since the mid-1960s. Chemically named O,O-Diethyl O-3,5,6-trichloropyridin-2-yl phosphorothioate, chlorpyrifos is also known as Dursban, Lorsban, Whirlwind, Warhawk and Eraser.
                    <br><br>
                    <strong><i>2.<u>Azoxystrobin</u>:-</i></strong><br> 
                    Azoxystrobin is also a fungicide, used for a variety of produce farming. Known by commercial names including Amistar and Heritage, azoxystrobin is effective against multiple fungi such as powdery mildew, late blight, apple scab and rusts. Absorbed through the banana tree's roots it moves through the leaves to keep fungus at bay. Referred to as Methyl (E)-2-{2-[6-(2-cyanophenoxy) pyrimidin-4-yloxy]phenyl}-3-methoxyacrylate), azoxystrobin has a low toxicity to most avian, aquatic and terrestrial animals.
                    <br><br>
                    <strong><i>3.<u>Thiabendazole</u>:-</i></strong><br> 
                    ​Thiabendazole, also known as the medications Mintezol and Tresaderm, is a fungicide and parasiticide that is used on banana planatations. Its chemical formula is C10H7N3S. Thiabendazole is the most common pesticide residue found in bananas, occuring in 48.1 percent of the bananas tested, according to the Pesticide Action Network North America. Although banana farmers use particularly high amounts of pesticides, with the Environmental Working Group putting the number at 35 pounds per acre, the residues aside from thiabendazole are particularly low because the banana peel protects the edible portion from many chemicals.
                    <br><br><strong><i>4.<u>Imidacloprid</u>:-</i></strong><br> 
                    Imidacloprid is a neonicotinoid, which acts as a neurotoxic insecticide. This chemical compound, 1-(6-chloro-3-pyridylmethyl)-N-nitroimidazolidin-2-ylideneamine, can be used as a seed treatment or applied topically, and is effective against soil, chewing and sucking insects, according to the National Pesticide Information Center, a joint body between the EPA and Oregon State University.
