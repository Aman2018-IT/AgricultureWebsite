    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 20px;">
                        1.<i><strong><u>Land Preparation</u>:</strong></i><br>
                         Land must be prepared by proper dig at 5m X 5m apart and the beds are fully filled with 20 kg of FYM, 125 g of urea and 25ml of chlorpyriphos. These are mixed with 30 cm soil and filled above 10 cm of ground level.<br><br>

                        2.<i><strong><u>Sowing</u>:</strong></i><br>
                        
                        --><u>Time of sowing</u>:T- budding is done in the first week of May. T -budded seedlings are ready for transplantation in field in the month of December-January.<br>  
                        --><u>Spacing</u>:For planting square system is used with spacing of 6.5 m x 6.5 m.<br>
                        --><u>Sowing Depth</u>:The seeds of peach tree must be sown in beds which are 5 cm in depth with 12-16 cm apart from each other.<br>
                        --><u>Method of sowing</u>:In the starting budding /grafting is done and then transplanting method is used for growing the plants in the main field.<br><br>

                       <strong><i>3.<u>Propagation</u>:</i></strong><br>
                       For propagation, grafting or budding method is used. For raising rootstock Sharbati, Khurmani are used. Peach tree needs heavy and regular pruning. Pruning must be done in last week of October. Water sprouts and other shoots must be eradicated. The newly peach tree should be planted about 35 inches height.
                        <br><br>

                        <strong><i>4.<u>Weed Control</u>:</i></strong><br>
                        These are regulated by manual weeding however it is tiring as well as expensive.  As peach roots are irregular, they can be often damaged by continual ploughing. Thus, usage of herbicides is excellent substitute.  A pre-emergence applying Diuron @ 800 gm-1 kg/acre or even post-emergence use of Glyphosate @ 6 ml/acre in 200 liters of water throughout February-March efficiently manages broad-leave weeds as well as usual grasses in new orchards.
                        <br><br>

                       <strong><i>5.<u>Irrigation</u>:</i></strong><br>After planting the trees, irrigation should be given immediately. In rainy season, these plants may not require watering. Drip irrigation is the best method for effective use of water. Irrigation should be given at some critical stages, in case of dry spell. Watering is important at flowering/budding stage and fruit development stage.
                       <br><br>

                       <strong><i>6.<u>Harvesting</u>:</i></strong><br>April to May is the peak month of harvesting peach crop. They reach good color and hard flesh that shows they are ready to harvest. Harvesting of peach crop is carried by twisting the fruit.              
                       <br><br>

                       <strong><i>7.<u>Post Harvest</u>:</i></strong><br>After harvesting they are stored in moderate temperature and can be used in processing squash.
