                            <strong><u>Irrigation Guidance for Guava Cultivation</u>:</strong><br>
                            Guava is mostly grown under rainfed conditions and irrigation is generally not practiced but wherever the irrigation is available, it should be given in summer and October/Novermber as it enhances the yield of guava tree by increasing fruit set.<br><br>

                            <strong><u>Intercropping of Guava</u>:</strong><br>
                            Guava orchards may be intercropped with short growing leguminous crops like mash, moong, guar and vegetables during pre-bearing period.<br><br>

                            <strong><u>Aftercare of Guava</u>:</strong><br>
                            -->Pre-harvest spray of calcium nitrate (2%) two weeks before harvest improve quality at the time of harvest of guava, whereas, the spray of 150 ppm silver nitrate at 5 weeks before harvest and another spray at three weeks after first spray increases the shelf life of guava satisfactorily upto 6 days at room temperature.<br>
                            
                            -->GA, 90 ppm, sprayed 30 days before harvest improves the fruit quality at harvest and also increases the shelf life of guava.<br><br>

                            <strong><u>Guava Harvesting and Yield</u>:</strong><br>
                            -->In guava cultivation, grafted plants come into bearing at the age of 3 years and peak harvesting periods are August-September for rainy season crop and Jan-Feb. for winter season crop.<br>

                            -->Guava develops best flavour and aroma only when they ripe on tree. A ten year old guava tree gives a yield, up to 100 kg of fruit.<br><br>

                            <strong><u>Crop regulation</u>:</strong><br>
                             -->The guava fruits produced in the rainy season are not of high quality and have poor shelf life. Spray 10 per cent urea or 600 ppm NAA during April-May when maximum flowers have opened.<br>

                            -->This eliminates rainy season crop so that winter guava crop is heavy and of superior quality. One leaf pair pruning of guava plants in the first week of may offers an effective method for avoiding rainy season crop, resulting into more yield and better quality in winter season crop.
