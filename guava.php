        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 20px;">
                            <strong><u>Guava Planting</u>:</strong>
                            The guava planting should be done in pits of 1m x 1m x 1m size filled with mixture of FYM and soil during rainy season (July-August) or in spring (February-March) at a distance of 6 m x 6 m.<br><br>

                             <strong><u>Propagation and Rootstock of Guava</u>:</strong><br><br>
                            --> Guava plants grown directly from seed give poor fruit yield and quality, and come into bearing late. Seeds should be used for rootstock raising. Subsequently, inarching or budding (patch or shield) can be done for propagation.<br>

                            --> In recent times, stooling has been found cheapest and easiest method of propagation. As seeds loose viability after extraction, they should be sown immediately.<br>

                            --> Soaking of seeds in water for 12 hours gives about 90 per cent germination. Time of guava propagation is Feb-March and July-August. Guava can also be successfully propagated by air layering.<br><br>

                            <strong><u> Training and Pruning</u>:</strong>
                            The plant should be trained to a low-headed system of open centre or delayed open centre keeping scaffold limbs having wide crotch angles. Pruning is not generally required every year, however, terminal shoots may be headed back to stop overcrowding of trees.<br><br>
                            
                             <strong><u>Manuring and Fertilization</u></strong>:
                            Although guava is grown without application of any manure and fertilizer, it responds well in their application by giving higher yield and better quality fruit.
                            
