        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation and sowing</u>:</i></strong><br>
                            Well pulverized bed should be prepared by ploughing land once up to 20-25 cm deep. Take two or three harrowing after ploughing operation. Soil should be leveled by planking operation for one to two times. Maintain sufficient moisture in soil before sowing. For planting, two methods are widely used, 1) Ridge and furrow method 2) Flat Bed method. <br> <br>

                            Time of sowing
                           Accurate planting time is necessary for obtaining good yield. Best time of planting is when maximum and minimum temperature is from 30-32 °C and 18-20 °C respectively. For early season crop complete sowing from 25th September to 10th October. For medium duration crop, sowing should be done in first week of October to third week of October. While for late duration crop, complete sowing from third week of October to first week of November.
                           For spring season, second fortnight of January is best time planting.<br> <br>

                            Spacing
                            For planting, use distance of 20 cm between tubers and 60 cm between ridges manually or mechanically. Planting distance vary with size of tubers. If diameter of tuber ranges from 2.5-3.5 cm, use planting distance of 60x15 cm where as if diameter of tuber is of 5-6 cm, use spacing of 60x40 cm.<br> <br>
 
                            Sowing Depth
                           Dig a trench of 6-8 inch deep and plant piece of potato with eye pointing up.<br> <br>

                          Method of sowing
                           For sowing, use tractor operated semi-automatic or automatic planter.
                            
                                 <br><br>
                        <p style="color: white;font-size: 17px;">
                            <strong><i>2.<u> Seed</u>:</i></strong><br>
                            Seed rate <br>
                            Use seed rate of 8-10 qtl/acre for small size tuber, 10-12 qtl/acre for medium size and 12-18qtl/acre for large size tubers. Use whole seeds for disease free quality seed production. <br> <br>
                            
                            Seed Treatment <br>
                            Select seed/tuber from reliable source. Choose medium size tuber of 25-125 gm weight for plantation purpose. For plantation purpose potato tuber after removing from cold storage are kept in cool and shady place for one to two weeks to allow emergence of sprouts. To get uniform sprouting, Treat tubers with Gibberellic acid@1 gm/10 ltr water for 1 hour then dry in shade and keep in aerated dim room for 10 days. Dip cut tubers in solution of 0.5% Mancozeb solution (5 gm/ltr of water) for ten minutes. It will prevent rotting of tuber in early plantation stage. To protect crop from rotting and black scurf disease treat whole and cut tubers with 6% Mercury solution (Tafasan)@0.25% (2.5 gm/ltr of water). <br><br>
