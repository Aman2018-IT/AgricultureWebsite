        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation and sowing</u>:</i></strong><br> <br>
                          
                            Plough land thoroughly and make land weed and clods free. Add well decomposed cow dung of 5-10ton/acre and mix well in soils at time of land preparation. Avoid use of undecomposed or free cowdung as it will leads to forking of fleshy roots.<br><br>

                            <strong> Time of sowing  </strong>  <br><br>
                            <strong>Pusa Himani:</strong>  Optimum time for sowing is January-February.<br><br>
                          <strong> Punjab Pasand: </strong>  Optimum time for sowing is from second fortnight of March where as for Pusa chetki optimum time for sowing is from April-August.<br><br>

                           <strong>Japanese white:</strong>  Optimum time for sowing is from November-December.<br><br> <br>

                             
                             <strong>Spacing</strong>
                              Use row to row spacing of 45 cm and plant to plant spacing of 7.5cm.<br><br>

                              <strong>Sowing Depth : </strong> 
                              For good growth, sow seeds at depth of 1.5cm <br><br>

                             <strong>Method of Sowing : </strong>  Method of Sowing
