        <h3 style=" font-size: 30px; text-align: left; color: white;"><strong><i>VARIETIES:</i></strong></h3>
        <p style="color: white;font-size: 20px;"><strong><i><u></u></i></strong>
            Japanese white: Optimum time for sowing is from November-December. It is introduced in India from Japan. In northern plains it is recommended for late sowing and in hilly areas recommended for cultivation in July-September. Roots are cylindrical and having pure white color. Gives average yield of 160qtl/acre. <br> <br>  

            Pusa chetki: Optimum time for sowing is from April-August. Early maturing variety suitable for seed production for Punjab region. Its roots are smooth, snow white color and medium long. It gives average yield of 105qtl/acre and seed yield of 4.5qtl/acre.<br> <br>

            Pusa Himani: Suitable for sowing in fortnight of January-February. Its root are white with green shoulders. Ready to harvest in 60-65days after sowing. Gives average yield of 160qtl/acre.<br> <br>

            Punjab Pasand: Optimum time for sowing is from second fortnight of March. It is early maturing variety, ready to harvest in 45days after sowing. Roots are long, white and free from hair. Suitable for sowing in main season as well as for off season. In main season, gives average yield of 215qtl/acre where as in off season gives average yield of 140qtl/acre.<br> <br>

            <strong>     Other state varieties: </strong>  <br> <br>

            Pusa Deshi: Suitable for sowing in northern plains. Roots are of pure white color. Ready to harvest in 50-55days after sowing.<br><br>
            
            Pusa Reshmi: Variety is suitable for early sowing. Ready to harvest in 50 to 60days.  <br><br>

            Arka Nishant: Long and pink root variety. Ready to harvest in 50-55days.<br><br>

            Rapid Red White Tipped: Very early maturing European table type variety. Ready to harvest in 25-30days. Roots are small and bright red colored with pure white flesh color.<br><br>

