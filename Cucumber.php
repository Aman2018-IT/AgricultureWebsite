                                <p style="color: white;font-size: 17px;"><strong><i>4.<u>Irrigation</u>:</i></strong><br>
                          
                                    In summer season it requires frequent irrigation and in rainy season it does not require any irrigation. In total it requires 10-12 irrigations. Pre-irrigation is required before sowing then subsequent irrigation is required after 2-3 days of sowing. After second sowing, the crops are then irrigated at the interval of 4-5 days. Drip irrigation is very useful for this crop.

                                   <br><br>


                               

                           
                            <p style="color: white;font-size: 17px;">
                                <strong><i>5.<u>Harvesting</u>:</i></strong><br>
                            
                                Plants start yielding in about 45-50 days after sowing. Mainly 10-12 harvestings can be done. Harvesting is mainly done when the seed of cucumber are soft and the fruits are green and young. Harvesting is done with the help of sharp knife or any sharp object. It gives an average of 33-42qtl/acre of yield.
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>6.<u>Seed Production</u>:</i></strong><br>
                                Brown color fruits are best for seed production. For seed extraction, fruit pulp is taken out in fresh water for 1-2 days for the easy separation of seeds. The seeds are then rubbed with hands and as a result heavy seeds will settle down in water and then they are preserved for further use.
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>7.<u>Plant Protection</u>:</i></strong><br>
                                <strong>Disease and their control:</strong> <br><br>

                              Anthracnose: It attacks almost all parts of the cucumber which are above the ground. The symptoms are yellowish color circular spots are seen on the older leaves and the circular and sunken spots are seen on the fruit. <br>
                              Treatment: Fungicidal application of Chlorothalonil and benomyl is given to protect the crop from disease.<br><br>
                                
                               Bacterial wilt: It is caused by Erwinia tracheiphila. It affects the vascular tissues of the plant which result in immediate wilting. <br>
                               Treatment: Spraying of foliar insecticides is done to cure the bacterial wilt.<br><br>

                               Powdery mildew: The symptoms are appearance of white powdery spots on upper surface of leaves which causes leaf withering. <br>
                               Treatment: Application of carbendazim@2gm in 1ltr of water will help to cure powdery mildew. It can also be controlled by fungicidal sprays of Chlorothalonil, benomyl or dinocap.<br><br>

                               Mosaic: The symptoms are stunted growth of plant, leaves turn down and fruit knobs become light yellow in color. <br>
                               Treatment: Application of Diazinon is done to cure mosaic disease. Use of Immidachloprid-17.8%SL @7ml in 10ltr of water is done to cure disease.<br><br>
                              
                               <strong>Pest and their control:</strong> <br> <br>
                               Fruit fly: It is serious pest found in cucumber. Females fly lay eggs below epidermis of young fruits. Later on maggots feed on pulp afterward fruits starts rotting and get drop. <br>
                               Treatment: Foliar application of Neem oil @3.0%is given to cure the crop from fruit fly pest.
