    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation </u>:</i></strong><br>
                            Prepared field by ploughing land, two - three times. After ploughing do planking operation. For turmeric planting, beds of 15cm height, 1m width and of convenient length are prepared. Keep distance of 50cm between beds.
                           
                                 <br><br>
                        <p style="color: white;font-size: 17px;">
                            <strong><i>2.<u>  Fertilizers</u>:</i></strong><br>
                            Farm yard manure @10 t/ha is applied as basal dressing. Beds are earthed up each time after top dressing. The NPK recommendation is 125:37:37kg per ha. Whole Phosphorus (P) and Potash (K2O) is given as basal dose at the time planting while Nitrogen (N) is applied as 25kg each at basal, 30,60,90 and 120 days after planting i.e., 125kg N. <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>3.<u>Irrigation</u>:</i></strong><br>Depending on the soil types, irrigated crops require 15-20 irrigations in heavy soils and 35-40 in light soils. Moisture stress affects the growth and development of the plant especially during the rhizome bulking stage .
                            
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>4.<u>Harvesting</u>:</i></strong><br>
                            
                                Depending upon the variety, the crop becomes ready for harvest in seven to nine months. The land is ploughed and the rhizomes are gathered by hand picking or the clumps are carefully lifted with a spade. Harvested rhizomes are cleaned of mud and other extraneous matter adhering to them. The average yield per hectare is 20-25 tonnes of green turmeric.
                            <br><br>
                            
                            <p style="color: white;font-size: 17px;">
                                <strong><i>5.<u>Pest and Diseases</u>:</i></strong><br>
                               <strong> Pest</strong>
                                Shoot borer (Conogethes punctiferalis) :The presence of bore hole on the pseudostems through which the frass is extruded and the withered central shoot are the symptoms of pest infestation.
                               <strong> Control</strong>
                                Spray Malathion 0.1% at monthly interval from July to October. <br> <br>
                                
                                Rhizome scale (Aspidiotus hartii):
                                They feed on the plant sap and in the field in severe cases plants wither and dry. In storage the pest infestation results in shrivelling of buds an rhizomes and may also affect the sprouting of rhizomes. <br> <br>
                               <strong>Control</strong> 
                                Dip the rhizomes in quinalphos 0.1% twice prior to storage and sowing. <br> <br>

                                <strong> Diseases </strong>

                                   Rhizome rot (Pythium graminicolum)  <br> <br>
                                   
                                    Collar region of the pseudostems become soft and water soaked, and the plant collapses.<br> <br>
                                  <strong>Control </strong> 
                                   Drench the soil with 0.3% Dithane M-45. Dip rhizomes in the same chemical solution for 30 minutes before storage and at the time of sowing.<br> <br>

                                   Leaf blotch (Taphrina maculans) :<br> <br>
                                   There are small, oval, rectangular or irregular brown spots on either side of the leaves. The leaves turn   yellow.<br> <br>
                                  <strong> Control</strong> <br> <br>
                                   Spray 0.2% Dithane M.45.

                                   Nematodes (Meloidogyne sp., Radopholus similis):<br> <br>
                                  <strong>Control</strong>         <br> <br>
                                  Apply to soil aldicarb or carbofuran granules at 1kg a.i/ha


