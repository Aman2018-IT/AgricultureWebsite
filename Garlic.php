        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation and sowing</u>:</i></strong><br>
                          
                            Give three to four deep ploughing and bring soil to fine tilth. Add well decomposed cow dung to increase organic content of soil. Then levelled soil and divide into small plots and channels.<br><br>

                            Time of sowing:
                            Optimum time for sowing is last week of September to first week of October.<br><br>

                            Spacing:
                            Keep spacing of 7.5 cm between two plant and 15 cm between the rows.<br><br>
 
                            Sowing Depth:
                            Sow garlic cloves 3 to 5 cm deep keeping their growing heads upward.<br><br>

                            Method of sowing:
                            For sowing of garlic use Kera method. Sowing can be done manually or with help of Machine. Cover cloves with soil and provide light irrigation.
                            <br><br>
                        <p style="color: white;font-size: 17px;">
                            <strong><i>2.<u>Seed</u>:</i></strong><br>
                            Seed Rate:
                            Use seed rate of 225-250 kg per acre.<br><br>
                            
                            Seed Treatment:
