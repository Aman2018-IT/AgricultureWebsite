                            <p style="color: white;font-size: 17px;">
                                <strong><i>6.<u>Pest and Diseases</u>:</i></strong><br>
                            
                              <strong>Pest and their control:</strong>   <br><br>

                             Thrips: If it is not controlled properly, can cause yield loss up to 50%. Mostly observed in dry weather. They suck sap from the foliage and results in curling of leaves, leaves become cup shaped or curved upward. <br><br>
                             Maggots: Infestation observed in January-February Month. They feed on roots cause browning of leaves. Base of plant become watery.


                             If infestation is observed, apply Carbaryl@4 kg or Phorate@4 kg to the soil and give light irrigation. Apply Chlorpyriphos @1 Ltr/acre alongwith irrigation water or sand. <br><br>

                             
                             <strong>Disease and their control 
                                :</strong>  <br><br>

