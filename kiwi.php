    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                     
                        <p style="color: white;font-size: 20px;">
                            <strong><i>1.<u>Land Preparation </u>:</i></strong><br>           

                            For the plantation of kiwi, steep land can be contoured into terraces. Plant should get maximum sunlight, so rows can be oriented to north-south direction. Best month for plantation of kiwi is January, so pitting and manuring should be done by December month.<br><br>

                            <strong><i>2.<u>   Plantation  </u>:</i></strong><br>
                         
                            Kiwi plants can be propagated by seed/seedling/grafting. And plantation is usually done in the month of January. For the plantation, arbor or pergola system framework is used with 6m distance in rows. And for the pollination in plants, male to female plants ratio is maintained as 1:5.<br><br>

                            <strong><i>3.<u>  Cold Protection  </u>:</i></strong><br> 
                            
                            During Spring and Autumn, protection from cold is necessary, otherwise it will damage kiwi vines. Wind machines and water sprinklers can be used to protect kiwi vines.  <br><br>
                            
                            <strong><i>4.<u>  Fertilizer Requirements  </u>:</i></strong><br>                           
                            
                            For the healthy growth of kiwi, required fertilizer amount is, 20kg farmyard manure, 0.5 kg NPK mixture containing 15% Nitrogen application every year. After 5 years, same dose of farmyard manure and NPK dose as explained, Nitrogen 850-900 g, Phosphorus 500-600 g and Potassium 800-900 g every year. <br><br>
                           
                            <strong><i>5.<u> Kiwi Pruning and Training</u>:</i></strong><br>
                            
                            And kiwi vines grow so much and become overcrowded, so for managing them, pruning is necessary.  Pruning should be started when you plant the vine. For the first year, main focus should be on straight growth and strong framework instead of focusing on how to cut a kiwi. Tie the vine loosely to the pole and keep it growing upward in straight manner. Vines should not be curled around the pole. All side branches should be pruned until vine reaches the top of the pole. Cut the top part of the vine and promote side shoots to grow laterally along the wires in framework. For pruning, winter is the best time. If the vine doesn’t form good side branches at the top, then cut the main branch by nearly 2 feet and try to grow it and form proper side branches at the top again next year.<br><br>  
                            
                            <strong><i>6.<u>  Irrigation </u>:</i></strong><br>
                          
                            Irrigation should be provided in September-October month, when the fruit is in the initial stage of growing. Irrigation in 10-15 days of interval is beneficial for healthy growth of plant and fruits. 
                            
                            Intercropping of legumes and vegetables can be done with the kiwi plants. And one thing can be noted that, no serious pests or diseases are observed in the cultivation of Kiwi, just proper and regular care is sufficient.<br><br> 
                            
                            <strong><i>7.<u>   Harvesting </u>:</i></strong><br>                          
                          
                            After 4-5 years, kiwi plants start bearing fruits, but commercial production starts at 6-7 years’ age. At low altitudes, fruits mature faster than at high altitudes because of temperature difference. Large fruits are harvested first and smaller fruits are given some time to grow. Hard fruits are covered with a coarse cloth and transported to the market and after some days, i.e., 1-2 weeks, they become soft and edible.<br><br> 
                            
                            <strong><i>8.<u>Yielding</u>:</i></strong><br>
                            Yield rate of 50-100 kg per vine is observed in Kiwi plants. 
