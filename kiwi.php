            <p style="color: white;font-size: 30px; text-align: left;"><i>Kiwifruit (commonly shortened to kiwi in North America) or Chinese gooseberry is the edible berry of several species of woody vines in the genus Actinidia.The most common cultivar group of kiwifruit (Actinidia deliciosa 'Hayward') is oval, about the size of a large hen's egg: 5–8 centimetres (2–3 inches) in length and 4.5–5.5 cm (1+3⁄4–2+1⁄4 in) in diameter. It has a thin, fuzzy, fibrous, tart but edible light brown skin and light green or golden flesh with rows of tiny, black, edible seeds. The fruit has a soft texture with a sweet and unique flavour. In 2018, China produced half of the world total of kiwifruit.</i></p>
        
        
