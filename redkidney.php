      <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
        <p style="color: white;font-size: 20px;">
        <strong><i><u>Land preparation</u></i></strong>:<br><br>
        Give two to three ploughing to bring soil at fine tilth. Make field level so that water stagnation should not occurred in main field. Crop is very sensitive to water logging. At last ploughing apply Farmyard Manure or well decomposed cow dung@60-80qtl/acre.<br><br>

        <strong><i><u>Sowing</u></i></strong>:<br><br>
        <strong><i>Time of sowing</strong></i><br>
        For spring season, best time for Kidney beans cultivation is February-March and for Kharif season, it is sown during May-June month. In Punjab, some farmers sow Kidney beans in last week of January.<br><br>  

       <strong><i>Spacing:</i></strong>For early sown varieties use spacing of 45-60 cm between rows and 10-15 cm between plants. For pole type varieties sown at distance of 1 m in hill@3-4plant per hill.<br>
       <strong><i>Sowing Depth:</i></strong>Sow the seeds at depth of 6-7 cm.<br>
       <strong><i>Method of sowing:</i></strong>For sowing dibbling method used. In plain area seeds are sown in line or on bed where as in hilly areas, seeds are sown on ridge.<br><br>

      <strong><i><u>Chemical weed control</u></i></strong>:<br><br>
      Initial growth period is crucial for crop growth. Avoid weed infestation at this stage. Complete weeding operations synchronized along with fertilizer and irrigation operations. Use Fluchloralin@ 800ml/acre or Pendimethalin@1ltr per acre as pre-emergence weedicide.<br><br> 
     
       <strong><i><u>Irrigation</u></i></strong>:<br><br>
       For better germination of seed give pre-sowing irrigation. 6-7 irrigations are required during growing season. Irrigation on 25th day after sowing and three irrigation at 25 days interval are necessary to get optimum yield. Give irrigation prior to blooming, during flowering and at pod development stage, water stress at these stage will lead to yield loss.<br><br>

       <strong><i><u>Harvesting</u></i></strong>:<br><br>
       Harvest when pods are full grown and ripe and there color turn to yellow. Also leaves turn yellow and majority of leaves drop. Depending upon variety use pods are ready to harvest 7-12 days after flowering. Overall crop is ready to harvest in 120-130 days. Do harvesting at right time as delay cause shattering. Keep harvested plant for three-four days in sun. After proper drying of crop, threshing is done with help of bullocks or with sticks.<br><br>

       <strong><i><u>Post-Harvest</u></i></strong>:<br><br>
       Kidney beans required little processing after harvesting but take care during storage to maintain good quality. Before storing, do sorting and remove damaged, infected beans. Heat and humidity cause deterioration in quality so always stored beans in cool, dark and dry place. 

