        <h3 style=" font-size: 30px; text-align: left; color: white;"><strong><i>Pesticides requirement(For weed Control):</i></strong></h3>
            
                    <strong><i><p style="color: white;font-size: 20px;">1.<u>Chloridazon (Pyramin)</u>:-</i></strong><br> 
                        Chloridazon is applied after planting but before emergence. It must be applied to a moist soil to be effective. So apply either after good rainfall or after irrigation. Apply the chemical as soon after planting as possible as any contact with the leaves of the seedlings will cause damage and a reduction of yields. Some varieties are more sensitive to Chloridazon than others. So expect some loss of seedlings with Crimson Globe for instance.<br>
                        All chemicals are sensitive to pH, organic soil content, clay content, temperature and humidity. Pyramin applied during warm conditions will result in poor weed control and possible lower yields.<br>
                        Chloridazon is designed to control most annual broadleaf weeds. The following weeds are controlled:<br>
                   <strong>  1. Quick weed such as Galinsoga parviflora<br>
                        2. Pig weed – Amaranthus spp.<br>
                        3. Cow thistle Sonchus spp.<br>
                        4. Purslane – Portulaca oleracea and Blue grass – Poa annua<br>
                        Goosefoot  Chenopodium spp. and goose-grass Eleusine africana are fairly well controlled.</strong>  
                    <br><br>
                    <strong><i>2.<u>Sethoxydim (Nabu)</u>:-</i></strong><br> 
                    Nabu is a post emergence herbicide for the control of annual grasses only. It must be applied when the grass is in the 2nd leaf to 3rd tiller stage. So they must be very young and small. Beetroot is tolerant to this chemical so it can be applied at any growth stage.
