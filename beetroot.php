    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation and sowing</u>:</i></strong><br>
                          
                            Being a cool season crop, beet root is raised during winter in plains and as a springsummer crop in hills by March-April. In plains, sowing is practiced during SeptemberNovember.
                            Land is ploughed to a fine tilth by thorough ploughing making it loose and friable. Clods
                            are to be removed completely. Apply well decomposed farmyard manure at the time of final
                            ploughing. Flat beds or ridges and furrows are prepared. Water-soaked ‘seed balls’ which
                            contain 2-6 seeds are drilled 2.5 cm deep in rows at spacing of 45-60 x 8-10 cm. 5-6 kg of
                            seeds is required for one hectare. Staggered sowing at 1-2 weeks interval ensures steady
                            supply of roots during the season.
                                 <br><br>
                        <p style="color: white;font-size: 17px;">
                            <strong><i>2.<u> Manures and fertilizers</u>:</i></strong><br>
                            On sandy soils, organic manure @ 25 t/ha is recommended. For an average soil, 60-70
                            kg N, 100-120 kg P and 60-70 kg K/ha is recommended. Entire farmyard manure, half of N and
                            full P and K should be applied basal at the time of land preparation prior to sowing and
                            remaining at 30-45 days after sowing. Nitrate sources of N are preferred to ammonium sources.
                            Beets have a relatively high boron requirement and its deficiency causes internal breakdown as
                            black rot or dry rot. <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>3.<u>Aftercare</u>:</i></strong><br>
                            Thinning is an essential operation when more than one seedling germinates from each
                            seed. Moist soil is essential for seed germination and for further growth. Usually 5-6 irrigations
                            are usually given during summer and three irrigations during winter in North Indian plains.
                            Field is usually kept weed free by light hoeing at early stage of crop. Swollen roots are
                            also to be covered with soil by earthing up.
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>4.<u>Harvesting</u>:</i></strong><br>
                            
                            Medium sized tubers are of great demand and tubers are harvested after attaining a
                            diameter of 3-5 cm. Harvesting is done 8-10 weeks after sowing by pulling the top with hand.
                            Later tops are removed, graded and marketed. In European countries, where small sized
                            bunches are in demand, tubers are tied in bundles of 4-6 with their tops. Over matured and
                            oversized tubers become woody and crack. Yield varies from 25 to 30t/ha and the tuber stores
                            well at 0o
                            C and 90% RH.
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>5.<u>Seed Production</u>:</i></strong><br>
                            Unlike other root crops, annual tropical types do not exist in beet root. All cultivars in
                            beet root are temperate biennial types and seed production is possible in hills 1400 m above
                            mean sea level. A low temperature of 4.4 to 7.7o
                            C for 6-8 weeks is required for flowers to
                            initiate. Usual method of seed production is root to seed method. In this method, seeds are
                            sown in July and well developed roots are dug out during November-December. After selection
                            of root tubers, top is trimmed without injuring crown. Then whole tubers of selected plants are
                            transplanted at a spacing of 60 x 45-60 cm in well prepared fields and irrigated. Harvesting is 
                            done during June-July. Cross pollination is due to self incompatibility. Being a cross pollinated
                            crop, that too wind pollinated, provide an isolation distance of 1000 m for certified seed
                            production and 1600 m for breeder and nucleus seed production. Average seed yield is 2.0
                            t/ha.
                            <br><br>
                            <p style="color: white;font-size: 17px;">
