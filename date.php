    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>            
                   <p style="color: white;font-size: 20px;"> <strong><u>Sowing Process</u>:</strong><br><br>
                            <i>Soil preparation</i>:  <br>
                            --> Plough soil 2-3 times. Level it and bring it to a fine tilth stage.<br>                             
                            --> Dig pits of 1m x 1m x 1m during summer as preparation for monsoon.<br>                             
                            --> Let the pits stay open for 15 days.<br>                            
                            --> Use well-rotten FYM (Farm Yard Manure) + soil to fill the pits.<br>                             
                            --> Do the plantation.<br><br>
   
                            <i>Best time for plantation</i>:
                            <br>--> February and March. This lets the plant take complete advantage of the hot summer months.<br>
                            --> However, in Kutch areas, where the biggest production is done in India, the plantation is done in the beginning of monsoon.<br><br>

                            <strong><u>Irrigation Requirement</u>:</strong><br>  
                            --> Light irrigation is required immediately after plantation. Date palm crop is resistant to drought and excessive irrigation. Both are harmful though.  <br>
                            --> Date palm likes constant moisture in soil. However, soil should not be water-logged. <br>
                            --> Irrigation is not required during monsoon. If there is flood, water should be drained out.<br>
                            --> In areas of high water table, 5-6 irrigations per year are enough.<br>
                            --> Take care to irrigate the soil just after plantation. Once the off-shoots emerge, you can decrease the frequency of irrigation.<br>
                            --> It is a good idea to use dry leaves and grasses for mulching. This helps to retain moisture in soil, especially in arid and semi-arid areas.<br><br>
                            
                            <strong><u>Intercropping </u>:</strong><br>
                            --> Date palm trees have a long life. Intercropping can be easily done with pulses like gram, peas, lentils, and vegetables. You can also grow pomegranate and papaya. While intercropping, make sure you have arrangement of additional irrigation and fertilizers.  
                            <br><br>

                            <strong><u>Pruning</u>:</strong><br> 
                            -->Pruning is essential to maintain enough number of green leaves for good growth and yield of date palm farming. Scientists believe that the tree should have 75-100 leaves at the bearing stage.<br>
                            -->This means, no pruning is required for the first 4-5 years, as only upto 20 leaves are formed annually.<br>
                            -->It is important to maintain sufficient leaf bunch ratio to improve fruit quality. High ration invites high humidity, which can lead to the development of Blacknose disorder in dates.<br><br>

                            <strong><u>Pollination </u>:</strong><br>
                            Date palm trees are dioecious in nature and highly cross-pollinated. Just 5-10% of male plants are enough to pollinate 100 female plants. Pollination is done through hand or machine in commercial date palm farming. About 3 male flower strands are inserted in female flower strands.<br>

                            Pollination is an important cultivation practice in date palm cropping, as it influences size and quality of dates palm fruits and time of ripening of dates.<br><br>

                            <strong><u>Fertilizers</u>:</strong><br>  
                            -->For rainfed areas, 30-40 kg FYM and 200 g each of Nitrogen, Phosphorus, and Potassium (NPK) per tree is required.<br>
                            -->For irrigated areas, 50-60 kg FYM and 200 g of NPK per tree is required. <br>
                            -->Apply whole of FYM, half of N, and full of P and K in September-October.<br>
                            -->Apply the remaining half of N in January-February. <br>
                            -->Green manuring can be done with rotting leguminous cover crops. <br><br>

                           <strong><u>Pest & Diseases</u>:</strong><br><br>  
                            <i><u>Pests</u>:</i><br>  
                            -->BlackheadedCaterpillar<br>
                            -->Red Weevil/Indian Palm Weevil<br>
                            -->Rhinoceros Beetle/Black Palm Beetle<br>
                            -->Scale Insects<br>
                            -->Birds<br>
                            
                            <strong><i>Diseases are False Smut/Graphiola Leaf Spot and other similar fungal diseases. Use of fungicide and Bordeaux mixture is helpful.</i></strong><br><br>

                            <strong><u>Harvesting</u>:</strong>  
                            Harvesting is done on the basis of growth and ripening stages. This is because dates are consumed at different stages of maturity.<br><br>  
                            
                            <i>You can harvest the fruit in the following stages of ripening</i>: <br>
                            --> When the fruit is still green and developing. It is known as Gandora or Kimri. <br>
                            --> When the fruit changes color from green to red or yellow and has achieved its full size. It is known as Dora or Khalal. <br>
                            -->When the fruit tip softens. It is known as Dang or Rutab.<br>
                            -->When the fruit is matured fully and dry enough to store without spoiling. It is called Pind or Tamar.<br><br>
                            
                            <strong><u>Yield </u>:</strong>
                            Date palm tree usually begins to bear fruit in the 4th year. It starts giving good yield (with proper care and cultivation practices) from the 5th year. <br><br>
                            
                            <i>Average-Yield</i>:<br>
                            -->A 10-year-old date palm tree can yield 50-60 kg fruits per year.<br>
                            -->A 15-year-old date palm tree can yield 80-200 kg fruits per year.<br>
                            -->Yield depends on variety, soil, area, and cultivation practices.<br><br>

                            <strong><u>Storage</u>:</strong><br>
                            Dates are usually harvested in the Khalal stage having high water content – upto 70-80%. They harbor poor keeping quality at room temperature. Cold storage is the best method to maintain its quality. Nonetheless, it is highly recommended to market them as soon as possible to maintain their freshness and crunchiness.<br><br>
                            Or they must be cured and processed to produce value-added products. Dried dates have a longer shelf life, upto a year.  
