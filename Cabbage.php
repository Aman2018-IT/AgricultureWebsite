    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                       
                        <p style="color: white;font-size: 17px;">
                            <strong><i>1.<u> Nursery Bed Preparation </u>:</i></strong><br>
                            The seeds are generally sown in a seed bed and 4-6 week old seedlings are transplanting to the field. Cabbage seeds are sown on nursery beds to raise seedlings for transplanting in the field. Raised beds of size 3 x 0.6 m and 10-15 cm in height are prepared. About 70cm distance is kept between two beds to carry out intercultural operations such as watering, weeding, etc. The surface of beds should be 2 smooth and well levelled. Well-decomposed FYM @ 2-3 kg/m is added at the time of bed preparation. Raised beds are necessary to avoid problem of water logging in heavy soils. To avoid mortality of seedlings due to damping off, drenching of the beds with Bavistin (15-20g/10 litres of water) is effective. <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>2.<u>Season of Planting</u>:</i></strong><br>
                                Sowing time depends upon the variety and the agro-climatic conditions prevailing in a particular region. Early cabbage is sown during July-November in plains and April-August in hills, as these require a longer period for their head formation.
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>3.<u>Raising of Seedlings </u>:</i></strong><br>
                                About 300-500g of seed are sufficient for raising nursery required to plant one hectare. Prior to sowing seeds are treated with fungal culture of Trichoderma viride (4 g/ kg of seed) or Thiram (3g/ kg of seed) to avoid damage from damping-off disease. Sowing should be done thinly in lines spaced at 5-7 cm distance. Seeds are sown at a depth of 1-2 cm and covered with a fine layer of soil followed by light watering by water can. The beds should then be covered with dry straw or grass or sugarcane leaves to maintain required temperature and moisture. The watering should be done by water can as per the need till germination is completed. The cover of dry straw or grass is removed immediately after emergence of seed sprout. If there is over crowding of seedling due to thick sowing, the extra seedlings should be thinned out. <br> <br>

                                The seedlings should be transplanted within 4-6 weeks of sowing. Older seedlings when transplanted result in poor growth and yield.
                                
                            <br><br>
                            <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land </u>:</i></strong><br>
                                The field is ploughed to fine tilth by giving four to five ploughing with a sufficient interval between two ploughing. Planking should be done for proper levelling. The transplanting is done on the flat land, ridges or in furrows depending on climate and soil conditions. For early planting, ridge method is suitable especially in areas where the rains occur at the time of planting. In saline soils, planting in furrows and in dry areas transplanting on flat beds is recommended.
                               
                                     <br><br>
                            <p style="color: white;font-size: 17px;"><strong><i>5.<u>Manure and Fertilizers </u>:</i></strong><br>
                                The fertilizer dose depends upon the fertility of soil and amount of organic manure applied to the crop. For a good yield, 15-20 tonnes of well-decomposed FYM is incorporated into the soil about 4 weeks before transplanting. Generally, application of 80-120 kg N, 60-100kg P2O5 and 60-120 kg K2O is recommended for optimum yield. Half the dose of N and entire amount of P and K is given at the time of transplanting. The balance N is given six weeks after transplanting or at the time of earthing up.

                               
                                     <br><br>
                                     <p style="color: white;font-size: 17px;"><strong><i>6.<u>Irrigation </u>:</i></strong><br>
                                First irrigation is given just after transplanting of seedlings and subsequent irritations are given at an interval of 10-15 days depending upon the season and soil conditions. Care should be taken to avoid water stress from the time of head formation to the head maturity period. At the time of crop maturity, irrigation should be avoided as excess irrigation at this stage causes splitting of heads.

                               
                                     <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>7.<u>Harvesting</u>:</i></strong><br>
                                Cabbage is ready for harvest at 90-120 days after planting. Cabbage should be harvested promptly when the heads are firm and mature. Delaying harvest, even a few days beyond maturity can result in split heads and increased incidence of field disease. <br> <br>

                                Harvesting immature heads, however, reduces yield, and the heads are too soft to resist handling damage. Immature heads also have a shorter shelf life than mature heads.  <br> <br>
                                
                                The head is harvested by bending it to one side and cutting it with a knife. The stalk should be cut flat and as close to the head as possible, yet long enough to retain two to four wrapper leaves. Extra leaves act as cushions during handling and may be desired in certain markets. The head should not be removed by snapping or twisting it since this practice damages the head and results in inconsistent stalk length. Broken stalks are also more susceptible to decay. As the heads are not ready for harvest at the same time, therefore they are harvested in stages based on the maturity of the heads. <br> <br>
                                
                                Harvested produce should always be stored in shade before packing. 
                           
