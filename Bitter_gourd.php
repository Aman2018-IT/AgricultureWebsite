            Bitter melons native to India have a narrow surface with pointed ends and are covered with triangular “teeth” and ridges. Bitter melons native to China are oblong with blunt ends and have a gently undulating, warty surface. <br>



Indian varieties include India Long Green, India Long White, Hybrid India Green Queen, and Hybrid India Pearl. <br> <br>

 <strong >Use:</strong> To prepare bitter melon, slice the fruit open and remove the seeds and pith. Do not peel. The fruit can be parboiled or soaked in salted water to lessen bitterness however this can affect the fruits normally crunchy texture.

Bitter melon can be stuffed (often stuffed with pork or shrimp and steamed), pickled, or curried and served with meat or in soup. The fruit pairs well with other strong flavors, like garlic, Chinese black beans, chili peppers, or coconut milk. <br> <br>

<strong >A dietary note: </strong> bitter melon is used in traditional Chinese medicine and in alternative medicine to treat Type 2 diabetes. It is also a folk remedy for treating high blood pressure. The combination of bitter melon and drugs sometimes used to treat hyperglycemia can decrease blood sugar levels to dangerously low levels.

Bitter melon has twice the beta carotene of broccoli, twice the potassium of bananas, and twice the calcium of spinach. It also contains high amounts of fiber, phosphorous, and Vitamins C, B1, B2, and B3.
