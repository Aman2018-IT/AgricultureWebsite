        <p style="color: white;font-size: 20px;">
                      
                   <strong>Pest and their control</strong>  
                   <strong> Fruit and Shoot Borer:</strong> It is one of the major and serious insect pests of brinjal. A short pinkish caterpillar bores into the terminal shoot and eats internal tissue in initial stages, later it bores into the young fruit. The large holes can be seen on the infected fruits. The insect affected fruits become unfit for consumption.

                    Scout field every week after transplanting for fruit and shoot borer infestation. Remove and destroyed infected fruits. Take spray of Triazophos@20ml/10Ltr water and Neem extract@50gm/Ltr, one month after transplanting. Repeat spray with interval of 10-15 days. When crop is in flowering stage take spray of Chlorantraniliprole 18.5%SC (Coragen)@5ml+Teepol@5ml in 12 Ltr of water, twice with 20 days interval.
                    
                    In initial stage of infestation, take spray of 5% Neem extract@50gm/Ltr. If infestation is seen in field take sprayed of 25% Cypermethrin@2.4 ml/10Ltr of water on affected crops. For high population spray Spinosad@1ml/Ltr of water. Avoid spraying of Triazophos or any other insecticide after fruit maturation and harvest.  </p>
                    
