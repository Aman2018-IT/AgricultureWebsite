            <p style="color: white;font-size: 30px; text-align: left; margin-left: 32px;"><i>Brinjal is one of the most common tropical vegetables grown in India. It is known by different names like Begun (Bengali), ringna (Gujarathi), baingan ( Hindi), badane (Kannada), waangum (Kashmiri), vange ( Marathi), baigan (Oriya), Vashuthana ( Malayalam), Kathiri (Tamil), venkaya (Telugu) and Peethabhala (Sanskrit). A large number of cultivars differing in size, shape and colour of fruits are grown in India. Immature fruits are used in curries and a variety of dishes are prepared out of brinjal. Fruits are moderate sources of vitamins and minerals like phosphorous, calcium and iron and nutritive value varies from variety to variety. </i></p>
       
        
