        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation and sowing</u>:</i></strong><br>
                            Plough land thoroughly and make land weed and clods free. Add well decomposed cow dung of 60-80 qtl/acre and mix well in soils at the time of land preparation. Avoid use of undecomposed or free cowdung as it will leads to forking of fleshy roots.

                            <strong> Time of sowing  </strong>  <br><br>
                            August-September is best time for sowing desi varieties whereas October-November month is ideal for European varieties. <br><br>

                             <strong>Spacing</strong>
                             Use row to row spacing of 45 cm and plant to plant spacing of 7.5 cm.

                              <strong>Sowing Depth : </strong> 
                              Sow seeds at depth of 1.5 cm.  <br><br>

                             <strong>Method of Sowing : </strong> 
                             Direct sowing on bed, in lines or on ridge.

                                 <br><br>
                        <p style="color: white;font-size: 17px;">
                            <strong><i>2.<u> Seed</u>:</i></strong><br>
                            <Strong> Seed Rate </Strong> <br> <br>
