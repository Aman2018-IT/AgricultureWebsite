    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Planting</u>:</i></strong><br>
                            1.Plant cherries in the late fall or early spring (when the ground is soft and has a higher moisture content).<br>
                            2.For sweet cherries, make sure the different varieties will pollinate each other.<br>
                            3.Plant in a sunny site with good air circulation; avoid planting near trees or buildings that shade.
                            <br>
                            4.Cherry trees need deep, well-drained soil. 
                            <br>
                            5.Space sweet cherries 35 to 40 feet apart; dwarfs, 5 to 10 feet apart.
                            <br>
                            6.Space tart cherries 20 to 25 feet apart; dwarfs, 8 to 10 feet apart.
                            <br>
                            7.Set trees on standard rootstocks with the graft union a few inches below the soil level. Set trees on dwarf rootstock with the graft union several inches above the soil level.
                            <br>
                            8.When planting fan-trained trees, construct the necessary supports before planting. Plant fans only 12 to 15 feet apart.
                            <br>
                            9.For bareroot trees, place the rootstock on a small mound of soil in the center of the planting hole, and spread the roots down and away, trying not to bend the roots. Backfiill with soil.
                            <br>
                            10.For container-grown trees, first remove the rootball and on its side; cut through the roots with shears. Don’t cover the top of the root-ball.
                        <br><br>

                        <strong><i>2.<u>Care</u>:</i></strong><br>
                        >>There is no difference in care between sour and sweet cherries.<br>
                        >>Apply mulch to retain moisture.<br>
                        >>Drape netting over trees to protect the fruit from birds.<br>
                        >>Water routinely in dry areas.<br>
                        >>Thinning the fruit is not necessary for cherry trees, as they typically thin naturally in early summer.<br>
                        >>Prune trees every year in late winter to encourage the growth of new fruiting wood. Don’t prune in the fall.<br>
                        >>Fertilize each spring until trees start to bear, then fertilize only after harvest each season. 
                        
                        <br><br><strong><i>3.<u>Pest/diseases</u>:</i></strong><br>
