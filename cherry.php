        <h3 style=" font-size: 30px; text-align: left; color: white;"><strong><i>Pesticides requirement:</i></strong></h3>
            
                    <strong><i><p style="color: white;font-size: 20px;">1.<u>Dormant-Sprays</u>:-</i></strong><br> 
                        Dormant sprays are the most important pesticide applications for cherry trees. These sprays help reduce the number of overwintering insects and pathogens that can damage the trees and reduce yield during the upcoming season. Early dormant sprays are applied in the fall when all of the leaves are gone. Lime-sulfur and dormant oils are applied for control of powdery mildew and overwintering insects. If bacterial canker (gummosis) is a problem, fixed copper sprays are applied. Chlorothalonil fungicide is sometimes used when Coryneum blight (shot-hole) is a problem, but copper sprays are also effective.

                        Delayed dormant sprays are applied when the buds begin to show green tissue. Horticultural oils are applied alone or with lime-sulfur for the control of insects and powdery mildew. When applying these dormant oils, it is important to make sure that the trees are thoroughly covered because the oils work by suffocating the attached insects.
                    <br><br>
                    <strong><i>2.<u>Pesticides sprayed at petal fall or shuck stage</u>:-</i></strong><br> 
                    Pesticide applications at petal fall can help prevent problems with insects, mites, shot hole disease and powdery mildew. Insecticidal soaps are soft pesticides for controlling mites and aphids. Biological pesticides made with Bacillus thuringenesis (for caterpillars) and Beauveria (mites and aphids) are also available.

                    Malathion is an organophosphate pesticide that can also be used for aphid control. Powdery mildew control requires a lime-sulfur spray and shot hole disease can be controlled by a wettable sulfur.
                    <br><br>
                    <strong><i>3.<u>Late Spring Control of Fruit Fly and Mites
                    </u>:-</i></strong><br> 
                    Pesticide sprays are not needed to control fruit fly infestations, instead use fruit fly bait. This bait attracts and kills fruit flies for several meters around where it is placed. If it is not available in your area, Malathion can be used. If there is a problem with both fruit flies and mites, use a biological product whose common name is Spinosad, but apply it in the evening to avoid harming bees. </p>
