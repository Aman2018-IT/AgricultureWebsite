                        >>Japanese beetles<br>
                        >>Caterpillars<br>
                        >>Brown Rot<br>
                        >> Black Knot<br>
                        >>Bacterial Canker (cut out any branches with signs of black knot or bacterial canker as soon as possible)<br>
                        >>Birds
                       
                       <br><br><strong><i>4.<u>Harvest</u>:</i></strong><br>
                       >> Pick fruits only when FULLY ripe (dark red, black, yellow); the sugar content rises the few days before fully ripened.<br>
                       >> Be ready to harvest within a week’s time. Eat or cook immediately.<br>
                       >>Pick fruits when firm if they are to be frozen.<br>
                       >> Be sure top pick with the cherry stem so you do not tear into the fruit, however, take care to lead the fruit spur to produce fruit next year.<br>
                       >>Hand-picking may injure the shoots and cause infection; Cut the stalks with scissors.<br>
                       >>Remember that cherry trees do not bear fruit until their fourth year. Thereafter, they should produce about 30 to 50 quarts of cherries each year.                       
