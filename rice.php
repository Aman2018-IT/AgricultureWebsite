        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 20px;">Rice cultivation is a complex activity that requires a series of processes to achieve the finished product.
                             <br><br>
                            The basic stages of cultivation include:<br><br>
                            
                        -->Seed selection<br>
                    -->Land preparation<br>
                -->Crop establishment<br>
            -->Water management<br>
        -->Nutrient managemen<br>
    -->Crop health management<br>
-->Harvesting<br>
-->Post harvest<br><br>
                            <u>SEED SELECTION</u>:<br>
                            Proper seed selection based on the cultivation environment is the first step towards ensuring that the highest possible yield is achieved.
                            <br><br>
                            Selecting good quality seed has many advantages and will help to:<br><br>
                            
                            Improve yield by 5 – 20%<br>
                            Improve germination by more than 80%<br>
                            Increase resistance to disease and pest attacks<br>
                            Maintain uniformity in plant size<br>
                            Have fewer weed problems<br><br>
                            <u>LAND PREPARATION</u>:<br>
                            The main purpose of land preparation is to have the soil in optimum physical condition for growing rice. Plowing and tilling of land is done to predetermined levels that allow rice plants to develop a good root system.
                            
                            The proper preparation of land for sowing is achieved via tractors (mechanical means) or with the help of water buffaloes. Land preparation also includes land leveling to ensure water reaches all areas planted.
                            <br><br>
                            <u>CROP ESTABLISHMENT</u>:<br>
                            Two main methods are used for introducing rice plants to the soil:
                            <br>
                            Transplanting<br>
                            Direct seeding<br>
                            Transplanting<br>
                            Transplanting is the more popular plant establishment technique in Sri Lanka and much of Asia. Transplanting occurs when pre-germinated seedlings are transferred from a seedbed to the wet field. It requires less seed and is an effective method for controlling weeds. A disadvantage is that the practice of transplanting is more labor intensive.
                            <br><br>
                            Prior to transplanting, seedlings are established in a separate nursery area. They grow between 20 and 80 days before being transplanted to the field. Seedlings can be transplanted by either machine or hand.
                            <br><br>
                            <u>Direct seeding</u>:<br>
                            Direct seeding is when dry seed or pre-germinated seeds and seedlings are sown by hand or planted by machine.
                            <br><br>
                            <u>WATER MANAGEMENT</u>:<br>
                            
                            
                            Cultivated rice is extremely sensitive to water shortages and when the soil water content drops below saturation, most rice varieties develop symptoms of water stress. Good water management practices are needed to keep usage at optimum levels and to maximize rice yield.
                            
                            Pre-planting steps that help with water management include;<br>
                            Proper creation and maintenance of field channels for water delivery<br>
                            Land levelling that allows water to be evenly distributed and retained<br>
                            Tilling operations that include rice field bund preparation, water puddling and maintenance<br><br>                            
                            <u>NUTRIENT MANAGEMENT</u>:<br>
                            Each growth stage of the rice plant has a different nutrient need. Keeping this in mind, farmers must ensure that the rice plant gets the proper nutrients at the right time.
                            
                            Prolonged flooding of rice fields ensures that farmers are able to conserve soil organic matter and also receive free input of nitrogen from biological sources. If higher yields are required, more nutrients must be added to the soil.
                            <br><br>
                            <u>CROP HEALTH MANAGEMENT</u>:<br>
                            The rice plant can come under threat from different sources in the field. These include attack by rodents, insects, weeds and disease. Farmers use many different strategies to protect and maintain crop health.
                            
                            A good understanding of pest behavior, natural enemies, host plants, other organisms and the environment help determine the type of pest management required.
                            
                            Farmers manage weed control through water management and land preparation, by hand weeding, and in some cases with the application of herbicides.
                            <br><br>
                            <u>HARVESTING</u>:<br>
                            Harvesting is the process of collecting the mature rice crop (rice paddy or rough rice) from the field. Depending on the variety, a rice crop usually reaches maturity at around 115-120 days after crop establishment.
                            <br>
                            Harvesting activity includes cutting, stacking, handling, threshing, cleaning, and hauling. Good harvesting methods help maximize grain yield and minimize grain damage and deterioration.
                            <br>
                            Harvesting can be performed manually or mechanically. Manual harvesting is common across Asia and involves cutting the rice crop with simple hand tools like sickles and knives.
                            <br>
                            Manual harvesting is very effective when a crop has fallen over. However, it is labour intensive. Manual harvesting usually requires 40 to 80 man-hours per hectare and it takes additional labour to manually collect and haul the harvested crop.
                            <br>
                            Mechanical harvesting using reapers or combine harvesters is the other option, but is not so common due to the availability and cost of machinery.
                            <br>
                            After cutting, the rice must be threshed to separate the grain from the stalk and cleaned. Threshing can be done by hand or machine.
                            <br><br>
                            <u>POST HARVEST</u>:<br>                          
                            
                            After harvest, the rice grain undergoes a number of processes depending on how it will be used. These steps include drying, storing, milling, and final processing.
                            <br>
                            Drying is the process that reduces grain moisture content to a safe level for storage. Drying is the most critical operation after harvesting a rice crop. Delays in drying, incomplete drying or ineffective drying will reduce grain quality and result in post harvest losses.
                            <br>
                            Proper storage conditions will ensure minimal loss of paddy rice due to changes in weather and moisture content, rodents, insects, microorganisms etc.
                            <br>
                            Milling of rice is a crucial post-production step. The basic objective of a rice milling system is to remove the husk and the bran layers and to produce an edible, white or brown rice kernel that is sufficiently milled and free of impurities.
