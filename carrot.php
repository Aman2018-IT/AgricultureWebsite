                                <strong><i>4.<u>Harvesting</u>:</i></strong><br>
                            
                                Early carrots are harvested when they are partially developed. For distinct markets, otherwise, they are retained in the soil till they reach the full maturity stage they should not be retained full maturity stage because they become hard and is unfit for consumption. <br><br>

                                Carrots are harvested when the roots are about 1.8 cm or larger in diameter at the upper end. The soil may be loosened with a special plow (carrot lifter) or an ordinary plow. <br> <br>
                                
                                The field is irrigated once a day before harvesting to facilitate harvest. <br><br>
                                
                                After harvesting, the carrots are placed in a packing house in crates before washing. After that, carefully wash Carrots and sort them by size before packaging.
