     <p style="color:white;font-size: 30px; text-align: left;">The lentil is an edible legume. It is an annual plant known for its lens-shaped seeds. It is about 40 cm tall, and the seeds grow in pods, usually with two seeds in each. As a food crop, the majority of world production comes from Canada and India, producing 58% combined of the world total.</p>

       <div class="Weather">
        <h2 style=" font-size: 30px; text-align: left; color: white;"><strong><i>Weather Conditions:</i></strong></h2>
            <p style="color: white;font-size: 20px;">Lentil requires cold climate. It is very hardy and can tolerate frost and severe winter to a great extent. It require cold temperature during its vegetative growth and warm temperature at the time of maturity. The optimum temperature for growth is 18-300 C.</p>      
       </div>

    <div class="Soil">    
        <h3 style=" font-size: 30px; text-align: left; color: white;"><strong><i>Soil requirement:</i></strong></h3>        
            <p style="color: white;font-size: 20px;">Well drained, loam soils with neutral reaction are best for lentil cultivation. Acidic soils are not fit for growing lentil. The soil should be friable and weed free so that seeding could be done at uniform depth. On heavy soils, one deep ploughing followed by two to three cross harrowing should be done.
            </p>
    </div>

