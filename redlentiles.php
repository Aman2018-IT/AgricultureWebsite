   <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
        <p style="color: white;font-size: 20px;">

         <strong><i><u>Sowing time</u></i></strong>:<br>
          --><strong><i>Recommended sowing time for Rainfed :</i></strong> First fortnight of October in Central and South India and second fortnight of October in North India;<br>
          --><strong><i>Under irrigated conditions :</i></strong> First fortnight of November in North India<br>
          --><strong><i>Late sowing :</i></strong> First week of December in rice fallows of NEPZ or on fields vacated very late by kharif crops under irrigated condition.<br><br>

         <strong><i><u>Seed Rate and Sowing</u></i></strong>:<br>
         --><strong><i>For small seeded :</i></strong> 40 - 45 kg/ha;<br>
         --><strong><i>For Bold seeded :</i></strong> 45 - 60 k g/ha;<br>
         --><strong><i>For Late sown condition :</i></strong> 50 - 60 kg/ha;<br>
         --><strong><i>For Utera cropping :</i></strong> 60 - 80 kg/ha seed is recommended.<br><br>
         
         Sowing should be done in rows 30 cm. apart and it should be sown at a lower depth (3 - 4 cm). This could be done either by using a Ferti-seed-drill or by seeding behind desi plough.<br><br>

         <strong><i><u>Plant nutrient management</u></i></strong>:<br><br>
         Generally Nitrogen 20 kg. Phosphorus 40 kg. and Sulphur 20 kg. per hectare in medium to low fertile soils as basal dressing. In lentil grown in calcareous alluvial soils, apply 1.6 kg of Boron per ha as basal to each crop. In soils, low in Zn, soil application of 20 kg Zinc Sulphate is recommended under rainfed and late sown condition; foliar spray of 2% urea improves yield.<br><br>

         <strong><i><u>Weed control</u></i></strong>:<br><br>
         Two manual weeding, one at 25-30 days and another 45-50 days after sowing should be done. Weedicide like Pendimethalin 30 EC @ 0.75 - 1 kg a.i. per hectare may be used as a pre-emergence treatment. A weed-free period of early 45 - 60 days is important.<br><br>

         <strong><i><u>Pest and disease management</u></i></strong>:<br><br>
         The major diseases of lentil are rust in northern plain and wilt in Central zone. Use of resistant varieties is helpful in controlling the disease - small seeded Rust resistant : PL-406, PL-639, PL-4, and L-4147 ;  bold seeded Rust resistant : L- 4076, DPL-15, IPL-406, DPL-62 etc.<br><br>

         Pest is not a major problem in lentil. In some years aphid population rises due to rise in temperature during January of February, and causes heavy damage. Spray of Monocrotophos (0.04%) is effective for aphid control.<br><br>


         <strong><i><u>Harvesting, threshing, storage</u></i></strong>:<br><br>
         Crop become ready for harvest when leaves begin to fall, stem and pod turn brown or straw in colour and seeds are hard and rattle with 15% moisture inside them. Over ripening may lead to fall of pods as well as shattering and seed cracking if seed moisture fall below 10% due to delay in harvesting.<br><br>

         The crop should be allowed to dry for 4-7 days on threshing floor and threshed by manually or bullock/power drawn thresher. The clean seed should be sun dried for 3-4 days to bring their moisture content at 9-10%. The seed should be safely stored in appropriate bins and fumigated to protect them from bruchids.<br><br>
         <strong><i>Yield</i>:-</strong> A well mange crop yields about 15 - 20 quintals of grain per hectare.
      </p>
