        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation and sowing</u>:</i></strong><br>
                          
                            Soil should be prepared by ploughing 2-3 times. After ploughing do levelling of soil for uniform bed formation. Prepared bed and irrigation channels.
                            <strong> Time of sowing  </strong>  <br><br>
                            Spinach is sown throughout the year. For winter season, best time for sowing is from September to October. For spring season complete sowing from Mid-February to April.<br><br>

                          

                             
                             <strong>Spacing</strong>
                             Use row to row spacing of 25-30 cm and plant to plant spacing of 5-10 cm.
 <br><br>

                              <strong>Sowing Depth : </strong> 
                              Sow seeds at depth of 3-4 cm.<br><br>

                             <strong>Method of Sowing : </strong> 
                             Sowing can be done by line sowing and broadcasting method.
                                 <br><br>
                                 <p style="color: white;font-size: 17px;">
                                    <strong><i>2.<u>Seed </u>:</i></strong><br>
                                   <strong>Seed rate : </strong> 
                                   For winter season use, seed rate of 4-6 kg and for summer crop use seed rate of 10-15 kg per acre.  
                                   <strong> Seed Treatment : </strong> 
                                   Before sowing soaked seeds in water for 12-24 hours. It will increased germination percentage.
                                    <br><br>
                        <p style="color: white;font-size: 17px;">
