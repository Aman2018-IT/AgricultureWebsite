                            For good yield, apply well decomposed cowdung@100 qtl and Nitrogen@35 kg (in form of Urea@75 kg), Phosphorus@12 kg (in form of Superphosphate@75 kg) per acre. Apply whole amount of well decomposed cowdung and phosphorus along with half amount of nitrogen before sowing. Apply remaining amount of nitrogen in two equal splits after each cutting. After fertilizer application provide light irrigation. <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>3.<u>Irrigation</u>:</i></strong><br>
                                For proper seed germination and good growth soil should have enough moisture content. At time of sowing if proper soil moisture is not present in soil then provide pre-sowing irrigation.
                                First irrigation should be given after sowing. In summer month, apply irrigation at interval of 4-6 days where as in winter month apply irrigation at interval of 10-12 days. Avoid over irrigation also care should be taken not to water on leaves, as it will leads to occurrence of disease and deterioration of quality. Drip irrigation is proves to be beneficial for spinach cultivation.
                            
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>4.<u>Harvesting</u>:</i></strong><br>
                                Depending upon variety, crop is ready for first cutting 25-30 days after sowing. For harvesting, use sharp knife or sickle. Depending upon variety and season, subsequent cutting should be done at interval of 20-25 days.  
                            
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>5.<u>Seed Production</u>:</i></strong><br>
                                Depending upon variety, crop is ready for first cutting 25-30 days after sowing. For harvesting, use sharp knife or sickle. Depending upon variety and season, subsequent cutting should be done at interval of 20-25 days.  
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>6.<u>Pest and Diseases</u>:</i></strong><br>
                                <strong>Pest and their control:</strong><br><br>
                                Aphid: If infestation is observed, take spray of Malathion 50EC@350 ml/ 80-100 ltr of water. Do not harvest crop immediately after spraying of Malathion. Harvest seven days after spray.
                                 <br><br>

                                
                                    <strong>Disease and their control:</strong> <br><br>
                                    ercospora Leaf spot: On leaves, small circular to semi-circular spots with grey centre and red margins appeared. On seed crop, if infestation is observed, take spray of Carbendazim@400 gm or Indofil M-45@400 gm in 150 ltr of water per acre. If necessary, repeat spray with intervals of 15 days.
