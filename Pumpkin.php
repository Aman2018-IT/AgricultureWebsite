                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land preparation and sowing</u>:</i></strong><br>
                          
                            Well prepared land is required for pumpkin farming. To bring the soil to fine tilth, ploughings with local tractor is required. <br> <br>


                            Time of sowing:  
                            February-March and June-July is an optimum time for seed sowing. <br> <br>

                            Spacing:
                            Sow two seeds on per hill and use spacing of 60cm. For hybrid varieties, sow seeds on both side of bed and use spacing of 45cm. <br> <br>

                            Sowing depth:
                            Seeds are sown 1 inch deep in the soil. <br> <br>

                            Method of sowing: 
                            •    Direct sowing 
