<div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
        <p style="color: white;font-size: 20px;">
        <strong><i>Recommended sowing dates for Black eyed bean Farming.</i></strong><br><br>

        <strong><u><i>The crop can be planted in different seasons</u>:-</i></strong><br><br>

        -->Kharif- sowing from June to July.<br>
        -->Rabi- from October to November. Ravi cultivation is mostly practiced in Southern parts of India.<br>
        -->Zaid- from April to May.<br><br>

       <strong><i><u>Land preparation for Black eyed bean Farming</u>:-</i></strong><br><br>

       Deep ploughing in the field should be done followed by two to three times harrowing. Planking should be followed by harrowing to level the field. Mix farmyard manure at the rate of 15 to 20 tons per hectare in the field during land preparation to increase the organic matter content and soil fertility.<br><br>

       <strong><i><u>Sowing methods and tips for Black eyed bean Farming</u>:-</i></strong><br><br>

       Black eyed beans can be sown by various methods such as broadcasting drilling and dibbling. The broadcasting method is usually practiced when the crop is cultivated for fodder purpose. In case of line sowing, the seeds are planted with the help of a seed drill at a spacing of 10 cm plant to plant distance and 30cm row to row distance. the depth of sowing should not be more than 2 to 4 cm.<br><br>

       <strong><i><u>Planting material required</u>:-</i></strong>:-<br><br>
        The seed rate required varies depending on the purpose for which crop is sown.<br>
        -->For fodder purpose- 30-34 kilogram seed per hectare is required.<br>
        -->For grain purpose- 20-25 kilogram per hectare of seed is required.<br><br>

      <strong><i><u>Planting material Treatment</u></i></strong>:-<br><br>
      The seeds are treated with thiram or captan at the rate of 2 gram per kilogram of seed before sowing. Rhizobium culture is also used to treat the seeds at the rate of 10 gram per kilogram of seeds.<br><br>

      <strong><i><u>Best cultivation practices for Black eyed bean Farming</u></i></strong>:-<br><br>

      -->Apply pre-emergence herbicide such as pendimethalin at the rate of 1 kilogram per hectare to control weed growth.<br>
      -->Mulching should be practised in the field to avoid water loss and maintain soil temperature.<br>
      -->Spray Quinalphos at the rate 2 ml per litre of water to control pod borer and hairy caterpillars.<br>
      -->To control aphids and jassids spray dimethoate 30 EC @ 1.5 ml per litre of water.<br>
      -->Do not practice cultivation in areas where sunlight is available for less than 6 hours.<br>
      -->Do not overwater the field.<br>
