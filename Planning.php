            Farm planning and estimation is a decision making process in the farm business, which involves organization and management of limited resources to realize the specified goals continuously. Farm planning involves selecting the most profitable course of action from among all possible alternatives.<br>

            <p style="color: white;font-size: 20px;"><Strong><u><i>Objectives of Farm Planning</i></u>:-</Strong></p>
            
            <p style="color: lightgray;font-size: 20px;">The ultimate objective of farm planning is the improvement in the standard of living of the farmer and immediate goal is to maximize the net incomes of the farmer through improved resource use planning. In short, the main objective is to maximize the annual net income sustained over a long period of time. The farm planning helps the cultivator in the following ways:- <br><br>
                a) It helps him examine carefully his existing resource situation and past experiences as a basis for deciding which of the new alternative enterprises and methods fit his situation in the best way.<br>
                b) It helps him identify the various supply needs for the existing and improved plans.<br>
                c) It helps him find out the credit needs, if any, of the new plan.<br>
                d) It gives an idea of the expected income after repayment of loans, meeting out the expenditure on production, marketing, consumption, etc.<br>
                e) A properly thought of a farm plan might provide cash incomes at points of time when they may be most needed at the farm.<br><br>
                A farm plan is a programme of total farm activities of a farmer drawn out in advance. An optimum farm plan will satisfy all the resource constraints at the farm level and yield the maximum profit.
