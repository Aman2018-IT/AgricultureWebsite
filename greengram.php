    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
        <p style="color: white;font-size: 20px;">
            <strong><u><i>Cultivation</i></u>:</strong><br>
            For the pure crop in the kharif season, the land is ploughed once or twice and harrowed to obtain a rough tilth. The summer crop can be sown dry in furrows in between the rows of the previous crop, followed by irrigation.<br><br>

            <strong><u><i>Sowing</i></u>:</strong><br>
            The kharif crop is sown in June to July, and the rabi crop in September or October. The spring crop is to be sown by 15 February and harvested by the middle of May. The summer crop is sown by 15 April. The seed may be sown by broadcasting or it may be drilled in furrows behind a plough, or with a three-or four-coultered desi drill, in rows 20-30cm apart. The seed-rate varies from 15-20kg per hectare when sown alone and for mixed crop 2-6kg per hectare.<br><br>

           <strong><u><i>Harvesting and yield</i></u>:</strong><br>
           To avoid loss because of the shattering of pods, the crop is harvested before it is dead ripe. One or two rounds of the picking of pods are also recommended to avoid losses because of the shattering. The plants are uprooted or cut with a sickle, are dried on the threshing-floor for a week or ten days and threshed by beating with sticks, and are winnowed with baskets. The average yield of grain from a pure crop varieties from 5-6q per ha, whereas yields up to 10-15q per ha.
        </p>
         
