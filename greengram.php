        <h3 style=" font-size: 30px; text-align: left; color: white;"><strong><i>Pesticides and fertilizers requirement:</i></strong></h3>
            
                    <p style="color: white;font-size: 20px;">
                       <strong><u>Fertilizers</u></strong>:<br>
                       Manuring with 25-40kg per ha of phosphorus (P2O5) and 25kg per ha of nitrogen (N) should be given at the time sowing. Also seed treatment with biofertilizer viz. Rhizobium at the rate of 25gram per kg of seed is beneficial.<br><br>

                       <strong><u>Pest</u></strong>:<br><br>
                       1. <strong><u><i>Aphids</i></u></strong>:<br>
                        The nymphs and adults suck the sap. The affected leaves turns yellow, get wrinkled and distorted. The insect also exude honeydew on which fungus develops, rapidly covers the plant with sooty mould that interferes with the photosynthetic activity of the plant.<br><br>
                        
                        <i>Control<i>:
                        Spraying with 0.05% Endosulfan, 0.02% Phosphamidon, 0.03% Dimethoate, Methyl demeton or Thiometon control the pest effectively.<br><br>

                      2. <strong><u><i>Thrips</i></u></strong>:<br>
                        The adults and nymphs feed on leaves. They scrape the epidermis and such the oozing sap. As a result, light brown patches appear on infested leaves. The affected leaves curl and become dry.<br><br>
                       
                       <i>Control<i>:
                       Spraying with 0.05% Endosulfan, 0.02% Phosphamidon, 0.03% Dimethoate, Methyl demeton or Thiometon control the pest effectively.<br><br>

                      3. <strong><u><i>Pod borer</i></u></strong>:<br>
                        Caterpillars feed on tender foliage and young pods. They make holes in the pods and feed on developing seeds by inserting anterior half portion of their body inside the pods.<br><br>
 
                       <i>Control<i>:
                        In early stage of attack handpicking of the caterpillars and their destruction. Ploughing fields after the harvest of crop would expose the pupae, which would be destroyed by birds. Spraying the crop with 0.05% Quinalphos or Fenitrohion can successfully control the pest. Spray with HaNPV @ 250 LE/ha.
                    </p>
    </div>

