    <div class="Cultivation">
        <h3 style="font-size: 50px; color:white;">CULTIVATION-PROCESS</h3>
                
                            
                        <p style="color: white;font-size: 17px;"><strong><i>1.<u>Land </u>:</i></strong><br>
                          
                            Broccoli can be grown in a wide variety of soils type. To get a better yield from the broccoli crop, sandy and silt loam soils are most preferred. The soil PH should be between  5.5 pH – 6.5 pH. <br> <br>

                            Before Planting broccoli, Land is plowed 3-4 times, then add compost or well rotten FYM 25-30 tons/ hectare and mix thoroughly at the time of land preparation.
                            
                           
                                 <br><br>
                         <p style="color: white;font-size: 17px;"><strong><i>2.<u> Plant seedling </u>:</i></strong><br>    
                            For creating broccoli plant seedlings, mainly two methods available<br> <br>

                            Soilless media – with the help of coco peat in the plastic nursery tray
                            Soil media – raised soil  bed.<br> <br>  <br>

                           1. prepare  1 meter wide and 3 meters long and 30 cm wide soil bed. <br> <br>
                           2. Mix roughly 10 kgs of good F.Y.M or compost manure into the soil in each bed. Likewise, add 50 grams of foret and 100 gm of Bavistin powder each spring and mix them in soils.<br> <br>
                           3. Then make 5 cm parallel to the 2 cm deep line’s width on the bed and sow broccoli seed. After that, cover the seeds with fine compost material.<br> <br>
                           4. Provide light water with the help of a sprinkler.<br> <br>
                           5. For one hectare, broccoli cultivating hybrid seeds requires approximately 312 grams.<br> <br>
                           6. Seed germination starts after 5 to 6 days, and seedlings and ready for transplantation within 35 days.<br> <br>
                           7. At this time the transplantation, the broccoli plant must have 4-5 leaves.<br> <br>
                           8. The best time sowing of seeds is the second week of September.<br> <br>
                           9. During plant growth, the temperature should be 20° C to 22 ° C.<br> <br>
                           10. for better growth for the seedlings, the ideal night and day temperature is  20° C to 23° C
                            While giving water to the nursery, Give quantity to be calcium nitrate and potassium nitrate, should be given to the plants mixed with 1.5 liters of water in one liter of water.<br> <br>
                           11 Similarly, every 10-12 days of malathion or disease should be prevented due to diseases and diseases that do not affect the plants. + Bavistin 1 gm, or copper oxychloride 1.5 g per liter of water should be sprayed.<br> <br>
                           
                           
                                 <br><br>
                        <p style="color: white;font-size: 17px;">
                            <strong><i>3.<u>  Fertilizers</u>:</i></strong><br>
                            Before giving fertilizer to the broccoli crop, the soil must be analyzed and then decide fertilizer dose quantity. <br> <br>

                            Generally, the broccoli crop is required to give 150 kg of Nitrogen, 100 kg of phosphorus, and 170kg of potassium per hectare. <br> <br>
                            
                            Nitrogen 120 kg, 80 kg phosphorus, and 60 kg potash should be applied at the transplantation time. The remaining half of nitrogen should be used in two split doses at 30 and 45 days after transplanting. <br> <br>
                            
                            Give micronutrients according to the crop requirement. broccoli plant show born deficiency, so it is observed in the field then apply born with foiler spray or with water fertilizer. <br><br>
                            <p style="color: white;font-size: 17px;">
                                <p style="color: white;font-size: 17px;"><strong><i>4.<u> Irrigation </u>:</i></strong><br>
                                Immediately after transplanting, give first irrigation. Depending upon soil, climatic condition, apply irrigation at interval of 7-8 days in summer season and 10-15 days during winter season.  <br> <br>

                                Also, rake the soil on the bed, which helps increase the oxygen level to the root zone.
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>5.<u>Weed Management</u>:</i></strong><br>
                                After the 30 days of transplanting, remove weed; this weed competes for food, sunlight, and air with the main crop, maintaining the crop weed-free. <br> <br>

                                Also, rake the soil on the bed, which helps increase the oxygen level to the root zone.
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>6.<u>Harvesting</u>:</i></strong><br>
                               1. Harvest broccoli in the morning, when the buds of the head are firm and tight, just before the heads flower.<br><br>
                               2. If you do see yellow petals, harvest immediately, as the quality will decrease rapidly.<br><br>
                               3. Cut heads from the plant, taking at least 6 inches of stem. Make a slanted cut on the stalk to allow water  to slide away. (Water can pool and rot the center of a flat-cut stalk, runing the secondary heads.)<br><br>
                                4.Most varieties have side-shoots that will continue to develop after the main head is harvested. You can   harvest from one plant for many weeks, in some cases, from spring to fall, if your summer isn’t too hot.<br><br>
                               5.  Learn more tips on harvesting broccoli.<br><br>

                         
                            <br><br>
                            <p style="color: white;font-size: 17px;">
                                <strong><i>7.<u>Storage</u>:</i></strong><br>
                               1. Store broccoli in the refrigerator for up to 5 days. <br> <br>
                               2. If you wash before storing, make sure to dry it thoroughly. <br> <br>
                               3.Broccoli can be blanched and frozen for up to one year. <br> <br>
                             
