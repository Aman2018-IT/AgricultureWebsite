        <h3 style=" font-size: 30px; text-align: left; color: white;"><strong><i>Pest/Diseases:</i></strong></h3>
            
                    <strong><i><p style="color: white;font-size: 20px;"><u></u></i></strong><br> 
                        1.Aphids: Curling leaves may mean that the plant’s sap is being sucked by insects. Apply soapy water to all sides of leaves whenever you see aphids. <br> <br>
                        2.Cabbage loopers: Small holes on the leaves between the veins mean small green caterpillars are present. Look at the undersides of the leaves. Hand pick if the problem is small or control with Bacillus thuringiensis, a natural, bacterial pesticide. Use a floating row cover just after planting through harvest to prevent caterpillars. <br><br>
                        3. Cabbage root maggots <br> <br>
                        4. Cabbageworms and other worm pests: Treat same as loopers. <br> <br>
                        5.Clubroot: Quickly wilting plants may be due to this fungus in the soil. The entire plant, including all roots and root tendrils, must be gently dug up and removed. If the roots are gnarled and misshapen, then clubroot is the problem. Act quickly to remove the plants so that the fungus doesn’t continue to live in the soil. Do not compost the plants. Raise the pH of your soil to above 7.2. You may need to sterilize your soil, too. <br> <br>
                       6. Downy Mildew: Yellow patches on leaves are usually caused by moist weather. Keep leaves as dry as possible with good air circulation. Buy resistant varieties. <br> <br>
                       7. White rust <br> <br>
                       8. Whiteflies <br> <br>
                        9.Nitrogen deficiency: If the bottom leaves turn yellow and the problem continues toward the top of the plant, the plants need a high nitrogen (but low phosphorus) fertilizer or blood meal. Blood meal is a quick nitrogen fix for yellowing leaves. <br> <br>
